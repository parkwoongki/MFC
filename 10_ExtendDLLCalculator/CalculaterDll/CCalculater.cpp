#include "CCalculater.h"


CCalculater::CCalculater()
{
}


CCalculater::~CCalculater()
{
}


double CCalculater::Add(double a, double b) {
	return a + b;
}


double CCalculater::Sub(double a, double b) {
	return a - b;
}


double CCalculater::Mul(double a, double b) {
	return a * b;
}


double CCalculater::Div(double a, double b) {
	return a / b;
}

